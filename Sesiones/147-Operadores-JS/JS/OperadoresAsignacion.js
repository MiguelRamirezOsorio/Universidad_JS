


//Operadores de Asignacion
//Operador "="
let a = 1;



//Operador compuesto de adicion "+="
a += 3; // a = a + 3
console.log( a );

//Operador compuesto de resta "-="
a -= 2; // a = a - 2
console.log( a );

//Operador compuesto de multiplicacion "*="
a *= 3;
console.log( a );

//Operador compuesto de division "/="
a /= 2;
console.log( a );

//Operador compuesto de modulo "%="
a %= 2;
console.log( a );

//Operador compuesto de exponente "**="
a **= 2;
console.log( a );


